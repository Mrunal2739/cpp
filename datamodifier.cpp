#include <iostream>
using namespace std;
int main(){
     /*

      -short,long data modifiers defines min and max size.
     - signed is default it can have positive and negative values.
      -unsingned can have only positive numbers.

     */
	short int i=34;
	signed int si=-267773;
	long int li=124732984570923846;
	unsigned int ui=7456;

	cout<<"short int: "<<i<<endl;
	cout<<"signed int: "<<si<<endl;
	cout<<"long int: "<<li<<endl;
	cout<<"unsigned int: "<<ui<<endl;


	return 0;
}
